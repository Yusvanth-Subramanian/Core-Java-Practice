public class Casting {

    public static void main(String args[]){

        short s = 1;

        // short to int
        int i=s;

        i=2;

        // int to short
        s = (short) i;

        // float to double
        double d = 10.5f;

        // double to float
        float f;
        f=(float) d;

        // int to long
        long l = i;

        // long to int
        i=(int) l;
    }
}
