public class EscapeSequence {

    public static void main(String args[]){

        System.out.println("Adding double quotations : \"");

        System.out.println("Adding single quotations : \'");

        System.out.println("Giving 4 spaces using tab :\tThis is after using \\t");

        System.out.println("Moving to the next line : \nThis is after using \\n");
    }
}
