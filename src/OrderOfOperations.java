public class OrderOfOperations {

    public static void main(String args[]){

        int a = 10 + 3 * 2;
        System.out.println("10 + 3 * 2 = "+a);

        a=(10+3)*2;
        System.out.println("( 10 + 3 ) * 2 = "+a);

        a=10^3*2;
        System.out.println("10 ^ 3 * 2 = "+a);
    }
}
