public class Switch {

    public static void main(String args[]){

        int i=2;

        switch (i){
            case 1:
                System.out.print(1);
                break;
            case 2:
                System.out.print(2);
                break;
            case 3:
                System.out.print(3);
                break;
            default:
                System.out.print("Default");
        }

    }
}
